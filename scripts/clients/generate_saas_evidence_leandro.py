def add_para(doc, text, bold=False, italic=False, color=None, size=11, alignment=None, space_after=6):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.bold = bold
    run.italic = italic
    if color:
        run.font.color.rgb = color
    if alignment:
        p.alignment = alignment
    p.paragraph_format.space_after = Pt(space_after)
    return p


def add_bullet(doc, text, bold_prefix=None):
    p = doc.add_paragraph(style="List Bullet")
    if bold_prefix:
        run_b = p.add_run(bold_prefix)
        run_b.bold = True
        run_b.font.size = Pt(11)
        run = p.add_run(text)
        run.font.size = Pt(11)
    else:
        p.runs[0].font.size = Pt(11) if p.runs else None
        if not p.runs:
            run = p.add_run(text)
            run.font.size = Pt(11)
    return p


